#有序可變動列表list[]
grades=[12,60,15,70,90]
print(grades[3])
#把55放到第一位
grades[0]=55
print(grades)
#移除列表第三第四位
grades[3:5]=[]
print(grades)
#增加兩個數字
grades=grades+[12,33]
print(grades)
#取得列表長度length
length=len(grades)
print(length)
#列表中放列表
data=[[3,4,5],[6,7,8]]
print(data[0][1])
print(data[1][0])
#列表內容變更
data=[[3,4,5],[6,7,8]]
data[0][0:2]=[5,5,5]
print(data)
#tuple不可變動列表()
data=(3,4,5)
print(data[0:2])
